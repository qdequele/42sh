#include <ft_sh.h>

char	*ft_remove_quote(char *word)
{
	if (word[0] == '\'' || word[0] == '"' || word[0] == '`'
		|| word[0] == '(')
		return (ft_strsub(word, 1, ft_strlen(word) - 2));
	return (word);
}
